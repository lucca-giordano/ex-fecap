package Prova;

public class Pessoa {
	
	//Atributos
	private String nome;
	private String CPF;
	
	//Construtor
	public Pessoa(String nome, String CPF) {
		this.nome = nome;
		this.CPF = CPF;
	}
	
	//Getters
	public String getNome() {
		return nome;
	}
	
	public String getCPF() {
		return CPF;
	}
	

}
